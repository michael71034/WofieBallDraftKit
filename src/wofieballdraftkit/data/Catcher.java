package wofieballdraftkit.data;

/**
 *
 * @author MiChAeL
 */
public class Catcher {
    int op;
    int r;
    int hr;
    double rbit;
    int sb;
    int ba;
    
    public Catcher(){
    super();
    
    }
    
    public int getOp() {
        return op;
    }

    public void setOp(int op) {
        this.op = op;
    }

    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    public int getHr() {
        return hr;
    }

    public void setHr(int hr) {
        this.hr = hr;
    }

    public double getRbit() {
        return rbit;
    }

    public void setRbit(double rbit) {
        this.rbit = rbit;
    }

    public int getSb() {
        return sb;
    }

    public void setSb(int sb) {
        this.sb = sb;
    }

    public int getBa() {
        return ba;
    }

    public void setBa(int ba) {
        this.ba = ba;
    }

}
